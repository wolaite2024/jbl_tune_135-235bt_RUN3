def generate_harman_gfps_info(color_id, model_id, public_key, private_key):
    gfps_info_format = f'<B{HARMAN_MODEL_ID_LEN}B{HARMAN_PUBLIC_KEY_LEN}B{HARMAN_PRIVATE_KEY_LEN}B'
    gfps_info = struct.pack(gfps_info_format, color_id, *model_id, *public_key, *private_key)
    return gfps_info
    
def generate_harman_product_info(product_id, device_name):
    #product_id = 0x1234
    device_name_bytes = device_name.encode("utf-8")
    device_name_padded = device_name_bytes.ljust(DEVICE_NAME_LEN, b'\x00')
    product_info_fmt = r"<40sH"
    product_info = struct.pack(product_info_fmt, device_name_padded, product_id)
    return product_info
    
